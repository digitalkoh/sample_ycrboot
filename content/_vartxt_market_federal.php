<p>A health insurance marketplace, or exchange, is a new way to shop for health insurance. A marketplace is a website that shows your options for private health insurance in one place. Your options will include plans from a variety of insurance carriers at a range of prices. All plans include a core set of health care services, including doctor visits, hospitalization, and drug coverage.</p>
<p>Beginning October 1, 2013, you can use the health insurance marketplace available to you to enroll in coverage that starts on January 1, 2014.</p>

